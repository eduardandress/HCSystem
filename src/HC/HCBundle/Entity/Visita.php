<?php

namespace HC\HCBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * Visita
 */
class Visita
{
    /**
     * @var integer
     */
    private $idvisita;

    /**
     * @var \DateTime
     */
    private $fecha;


    /**
     * Get idvisita
     *
     * @return integer 
     */
    public function getIdvisita()
    {
        return $this->idvisita;
    }

    /**
     * Set fecha
     *
     * @param \DateTime $fecha
     * @return Visita
     */
    public function setFecha($fecha)
    {
        $this->fecha = $fecha;

        return $this;
    }

    /**
     * Get fecha
     *
     * @return \DateTime 
     */
    public function getFecha()
    {
        return $this->fecha;
    }
}
